    public void updateStaffTable(){
        try {
        DefaultTableModel tableModel = new DefaultTableModel();
        
        String query = "SELECT `id`,`name` FROM `staff`";
        ResultSet rs = database.statement.executeQuery(query);

        tableModel.addColumn("NIC number");
        tableModel.addColumn("Staff Name");
        
        while(rs.next()){
        // add row by row
        Vector projectDetails = new Vector();

        projectDetails.add(rs.getString("id"));
        projectDetails.add(rs.getString("name"));

        tableModel.addRow(projectDetails);

        }

        staffTable.setModel(tableModel);

        } catch (SQLException e){
            e.printStackTrace();
        }
        
    }

