    private void animationEnter(java.awt.event.MouseEvent evt) {                                         
        bgColor = evt.getComponent().getBackground();
        panelHeight = evt.getComponent().getHeight();
        panelWidth = evt.getComponent().getWidth();
        panelPosition = evt.getComponent().getLocation();
        
        a = new Animate((JPanel) evt.getComponent(),evt.getComponent().getBackground());
        a.start();
    }             
   
     private void animationExited(java.awt.event.MouseEvent evt) {                                        
        // TODO add your handling code here:
        evt.getComponent().setBackground(bgColor);
        evt.getComponent().setSize(panelWidth, panelHeight);
        evt.getComponent().setLocation(panelPosition);
        
    }

