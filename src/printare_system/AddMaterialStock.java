    private void loadMaterialTypes(){
        try{
            String query = "SELECT `name` FROM `material_detail`";
            ResultSet rs = database.statement.executeQuery(query);
            
            Vector materialList = new Vector();
            while(rs.next()){
                materialList.add(rs.getString("name"));
            }
            
            materialType.setModel(new DefaultComboBoxModel(materialList));
            
        }catch(SQLException e){
            e.printStackTrace();
        }
    }
    
