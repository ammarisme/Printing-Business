    private void addToTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToTableActionPerformed
        // add details to the table
        if (!validateInput()){
            return ;
        }
        if (Integer.parseInt(quantity.getText())==0){
         return ;
     }
        DefaultTableModel GRN = (DefaultTableModel) materialTable.getModel();

        Vector productDetails = new Vector();
        materialInStock++;

        productDetails.add(materialInStock);
        productDetails.add(materialType.getSelectedItem());
        productDetails.add(quantity.getText());
        GRN.addRow(productDetails);

        materialTable.setModel(GRN);
    }//GEN-LAST:event_addToTableActionPerformed

    public void updateMaterialsTable(){
        try{
        String query = "SELECT * FROM `material_detail`";
        ResultSet rs = database.statement.executeQuery(query);
        
        DefaultTableModel tableModel = new DefaultTableModel();
        
        // add columns to table model
        tableModel.addColumn("Material Name");
        tableModel.addColumn("Stock Available");
        
        // add rows
        Vector rows = new Vector();
        while(rs.next()){
            rows.add(rs.getString("name"));
            rows.add(rs.getString("stock"));
            
            tableModel.addRow(rows);
            // nullify the vector
            rows = new Vector();
        }
        
        materialStockTable.setModel(tableModel);
        }catch(SQLException ex){
        ex.printStackTrace();
        }
    }
    private void addStockToSystemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addStockToSystemActionPerformed
  try {
        // create an entry in material load table
        String entry = "INSERT INTO `material_load` (`supplier_id`) VALUES('1')";
        database.statement.executeUpdate(entry);
       
        // get the material load id
        String query = "SELECT max(`id`) FROM `material_load`";
        ResultSet rs = database.statement.executeQuery(query);
        rs.next();
        int id = rs.getInt("max(`id`)");

        // update all the entries in material_of_load Table
        for (int rows = 0; rows < materialTable.getRowCount(); rows++){
            String materialType = (String) materialTable.getValueAt(rows, 1);

        // get stock of the current product and update the stock
            query = "SELECT `stock` FROM `material_detail` WHERE `name`='"+materialType+"'";
            rs = database.statement.executeQuery(query);
            rs.next();
            int stock = rs.getInt("stock");
            Integer newStock = stock + Integer.parseInt((String) materialTable.getValueAt(rows, 2));
            String update = "UPDATE `material_detail` SET `stock`='"+newStock+"' WHERE `name`='"+((String) materialTable.getValueAt(rows, 1))+"'";

            database.statement.executeUpdate(update);
            System.out.println("product added");

        // add entry to product in material of load table
            entry = "INSERT INTO `material_of_load` (`material_load_id`, `material_name`) VALUES('"+id+"','"+materialType+"')";
            database.statement.executeUpdate(entry);

            updateMaterialsTable();
        }
        
        resetTable();
        } catch(SQLException ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_addStockToSystemActionPerformed

